-- Test the UnixSocket sink: create a source, write to a unix socket, then drop the query.
CREATE LOGICAL SOURCE endless(ts UINT64);
CREATE PHYSICAL SOURCE FOR endless TYPE Generator SET(
       'ALL' as `SOURCE`.STOP_GENERATOR_WHEN_SEQUENCE_FINISHES,
       'CSV' as PARSER.`TYPE`,
       10000000 AS `SOURCE`.MAX_RUNTIME_MS,
       'emit_rate 10' AS `SOURCE`.GENERATOR_RATE_CONFIG,
       1 AS `SOURCE`.SEED,
       'SEQUENCE UINT64 0 10000000 1' AS `SOURCE`.GENERATOR_SCHEMA);
CREATE SINK socketSink(ENDLESS.TS UINT64) TYPE UnixSocket SET('/tmp/nes-test.sock' as `SINK`.SOCKET_PATH, 'CSV' as `SINK`.OUTPUT_FORMAT);
SHOW QUERIES;
SELECT TS FROM ENDLESS INTO SOCKETSINK SET ('a1041672-d4c9-40ac-a2b7-6b11342764b4' AS `QUERY`.`ID`);

SHOW QUERIES WHERE id = 'a1041672-d4c9-40ac-a2b7-6b11342764b4';
DROP QUERY WHERE id = 'a1041672-d4c9-40ac-a2b7-6b11342764b4';

SHOW QUERIES;
