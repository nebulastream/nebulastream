CREATE LOGICAL SOURCE sensor(
    id UINT64 NOT NULL,
    vi32 INT32 NOT NULL,
    vi8 INT8 NOT NULL,
    vu16 UINT16 NOT NULL,
    vf32 FLOAT32 NOT NULL,
    vf64 FLOAT64 NOT NULL,
    ni64 INT64,
    ni32 INT32,
    nf32 FLOAT32,
    nf64 FLOAT64,
    rstr VARSIZED);
CREATE PHYSICAL SOURCE FOR sensor TYPE Generator SET(
       'ALL' AS `SOURCE`.STOP_GENERATOR_WHEN_SEQUENCE_FINISHES,
       'CSV' AS PARSER.`TYPE`,
       10000 AS `SOURCE`.MAX_RUNTIME_MS,
       'emit_rate 100000' AS `SOURCE`.GENERATOR_RATE_CONFIG,
       42 AS `SOURCE`.SEED,
       'SEQUENCE UINT64 0 100000000 1, SEQUENCE INT32 0 100000000 1, SEQUENCE INT8 0 100 1, SEQUENCE UINT16 0 60000 1, SEQUENCE FLOAT32 0.0 100000000.0 1.0, SEQUENCE FLOAT64 0.0 100000000.0 0.5, SEQUENCE INT64 0 100000000 1, SEQUENCE INT32 0 100000000 1, SEQUENCE FLOAT32 0.0 100000000.0 1.0, SEQUENCE FLOAT64 0.0 100000000.0 0.5, RANDOMSTR 10 500' AS `SOURCE`.GENERATOR_SCHEMA);
CREATE SINK file_out(
    SENSOR.ID UINT64 NOT NULL,
    SENSOR.VI32 INT32 NOT NULL,
    SENSOR.VI8 INT8 NOT NULL,
    SENSOR.VU16 UINT16 NOT NULL,
    SENSOR.VF32 FLOAT32 NOT NULL,
    SENSOR.VF64 FLOAT64 NOT NULL,
    SENSOR.NI64 INT64,
    SENSOR.NI32 INT32,
    SENSOR.NF32 FLOAT32,
    SENSOR.NF64 FLOAT64,
    SENSOR.RSTR VARSIZED) TYPE ArrowFile SET(
       'ARROW_FILE_PATH' AS `SINK`.FILE_PATH);
SELECT ID, VI32, VI8, VU16, VF32, VF64, NI64, NI32, NF32, NF64, RSTR
FROM SENSOR
WHERE (ID / UINT64(1000)) % UINT64(2) == UINT64(0)
INTO FILE_OUT SET ('arrow-file-test-query' AS `QUERY`.`ID`);
